# Uppgift 5 — Miniräknare
# Låt användaren skriva in två tal och skriv ut resultatet av alla fyra räknesätt.

tal1 = int(input("Skriv ett tal: "))
tal2 = int(input("Skriv ett till tal: "))

print(f"{tal1} + {tal2} = {tal1 + tal2}")
print(f"{tal1} - {tal2} = {tal1 - tal2}")
print(f"{tal1} * {tal2} = {tal1 * tal2}")
print(f"{tal1} / {tal2} = {tal1 / tal2}")
